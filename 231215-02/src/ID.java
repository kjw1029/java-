
public class ID {
	private String ID;
	private String pass;
	public ID(String iD, String pass) {
		super();
		ID = iD;
		this.pass = pass;
	}
	public String getID() {
		return ID;
	}
	public String getPass() {
		return pass;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	
	
	
}
