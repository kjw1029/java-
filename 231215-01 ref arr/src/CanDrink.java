
public class CanDrink {
	private String naem;
	private int price;
	
	public CanDrink(String naem, int price) {
		this.naem = naem;
		this.price = price;
	}

	public String getNaem() {
		return naem;
	}

	public int getPrice() {
		return price;
	}

	public void setNaem(String naem) {
		this.naem = naem;
	}

	public void setPrice(int price) {
		this.price = price;
	}
	
	
}
